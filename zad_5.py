a = int(input("Введитте проверяемое число - "))
print('Число -',a)
if a%5==0: print ('- кратно 5')
else: print ('- не кратно 5')
if a%10==0:print ('- кратно 10')
else:print ('- не кратно 10')
if a%15==0:print ('- кратно 15')
else:print ('- не кратно 15')
if a%30!=0:print ('- не кратно 30')
else:print ('- кратно 30')